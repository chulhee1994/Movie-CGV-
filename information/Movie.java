package movie.information;

public class Movie {
	public String num;
	public String movieNum;
	public String movieName;
	public String genre;
	
	public Movie(String num, String movieNum, String movieName, String genre ) {
		this.num = num;
		this.movieNum = movieNum;
		this.movieName = movieName;
		this.genre = genre;
		
	}
	
}
